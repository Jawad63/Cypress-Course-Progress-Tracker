// JS has 7 primary data types: string, number, boolean, array, object, symbol and null.

var name = "john"; // string
var age = 29; // number
var isMarried = true; // boolean
var hasKids = ["timmy", "jimmy"]; // array
var info = {name: "john", age: 29} // object
var symbol = Symbol(); // symbol
var hasCar = null; // null

/* 
Number: Represents numeric values like 29 or 3.14.
String: Represents sequences of characters enclosed in quotes, like "Hello" or 'World'.
Boolean: Represents either true or false, used for logical operations.
Object: Represents a collection of key-value pairs, like a container for storing various properties and their values.
Array: Represents an ordered list of values, accessible by their index, like a shopping list.
Symbol: Represents a unique and immutable value, often used as identifiers for object properties.
Null: Represents the intentional absence of any object value, like an empty container.
*/

